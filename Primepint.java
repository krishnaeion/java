package kluy;



public class Primepint {

	public static void main(String[] args) {
	 int i=10,j=2;
	 for(i=10;i<=99;i++)
	 {
		 for(j=2;j<=i;j++)
		 {
			 if(i%j==0)
			 {
				 System.out.println(" ");
			 }
			 else
			 {
				 System.out.println(""+i);
			 }
		 }
	 }
		
	        

	}

}
