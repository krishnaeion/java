package kluy;
public class Switch {

	public static void main(String[] args) {
		int n=args.length;
		switch(n){  
		    case 0: System.out.println("10");break;  
		    case 2: System.out.println("20");break;  
		    case 3: System.out.println("30");break;  
		    default:System.out.println("Not in 10, 20 or 30");  
		    }  
		}  
		

	}


